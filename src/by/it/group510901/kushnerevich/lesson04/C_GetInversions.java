package by.it.group510901.kushnerevich.lesson04;

import java.io.FileNotFoundException;
import java.io.InputStream;
import java.util.Scanner;

/*
Рассчитать число инверсий одномерного массива.
Сложность алгоритма должна быть не хуже, чем O(n log n)

Первая строка содержит число 1<=n<=10000,
вторая - массив A[1…n], содержащий натуральные числа, не превосходящие 10E9.
Необходимо посчитать число пар индексов 1<=i<j<n, для которых A[i]>A[j].

    (Такая пара элементов называется инверсией массива.
    Количество инверсий в массиве является в некотором смысле
    его мерой неупорядоченности: например, в упорядоченном по неубыванию
    массиве инверсий нет вообще, а в массиве, упорядоченном по убыванию,
    инверсию образуют каждые (т.е. любые) два элемента.
    )

Sample Input:
5
2 3 9 2 9
Sample Output:
2

Головоломка (т.е. не обязательно).
Попробуйте обеспечить скорость лучше, чем O(n log n) за счет многопоточности.
Докажите рост производительности замерами времени.
Большой тестовый массив можно прочитать свой или сгенерировать его программно.
*/

public class C_GetInversions {

    public static void main(String[] args) throws FileNotFoundException {
        // Читаем входные данные из файла dataC.txt
        InputStream stream = C_GetInversions.class.getResourceAsStream("dataC.txt");
        C_GetInversions instance = new C_GetInversions();

        // Вычисляем количество инверсий в массиве
        int result = instance.calc(stream);

        // Выводим результат
        System.out.print(result);
    }

    int calc(InputStream stream) throws FileNotFoundException {
        // Подготовка к чтению данных
        Scanner scanner = new Scanner(stream);

        //!!!!!!!!!!!!!!!!!!!!!!!!!     НАЧАЛО ЗАДАЧИ     !!!!!!!!!!!!!!!!!!!!!!!!!

        // Считываем размер массива
        int n = scanner.nextInt();

        // Создаем и заполняем массив
        int[] a = new int[n];
        for (int i = 0; i < n; i++) {
            a[i] = scanner.nextInt();
        }

        //!!!!!!!!!!!!!!!!!!!!!!!!     тут ваше решение   !!!!!!!!!!!!!!!!!!!!!!!!!

        // Создаем временный массив для слияния (потребуется при сортировке)
        int[] temp = new int[n];

        // Запускаем модифицированную сортировку слиянием,
        // которая одновременно считает количество инверсий
        return mergeSortAndCount(a, temp, 0, n - 1);
    }

    /**
     * Рекурсивная функция сортировки слиянием с подсчетом инверсий
     * Алгоритм основан на том факте, что при слиянии двух отсортированных массивов
     * мы можем подсчитать инверсии между ними за линейное время
     *
     * Принцип подсчета инверсий:
     * Если элемент из правой части меньше элемента из левой части,
     * то он образует инверсии со всеми оставшимися элементами в левой части,
     * так как левая часть отсортирована и все оставшиеся элементы в ней больше текущего
     *
     * @param a - исходный массив
     * @param temp - временный массив для слияния
     * @param left - левая граница текущего подмассива
     * @param right - правая граница текущего подмассива
     * @return количество инверсий в текущем подмассиве
     */
    int mergeSortAndCount(int[] a, int[] temp, int left, int right) {
        int invCount = 0; // Счетчик инверсий для текущего подмассива

        // Базовый случай: если left == right, то подмассив состоит из одного элемента,
        // инверсий в нем нет, и рекурсия останавливается
        if (left < right) {
            // Находим середину подмассива
            int mid = left + (right - left) / 2;

            // Рекурсивно считаем инверсии в левой половине [left, mid]
            invCount += mergeSortAndCount(a, temp, left, mid);

            // Рекурсивно считаем инверсии в правой половине [mid+1, right]
            invCount += mergeSortAndCount(a, temp, mid + 1, right);

            // Считаем инверсии между левой и правой половинами при их слиянии
            invCount += mergeAndCount(a, temp, left, mid, right);
        }

        return invCount;
    }

    /**
     * Функция слияния двух отсортированных подмассивов с подсчетом инверсий
     *
     * Ключевая идея:
     * При слиянии двух отсортированных массивов, если мы берем элемент из правого массива,
     * то он меньше текущего элемента из левого массива (иначе бы мы взяли из левого).
     * Значит, этот элемент образует инверсию со всеми оставшимися элементами в левом массиве,
     * потому что левый массив отсортирован, и все оставшиеся элементы в нем больше текущего.
     *
     * Пример:
     * Левая часть: [2, 3, 9]
     * Правая часть: [2, 9]
     *
     * При слиянии:
     * - Берем 2 из левой (инверсий нет)
     * - Берем 2 из правой - он образует инверсии с [3, 9] (2 инверсии)
     * - Берем 3 из левой
     * - Берем 9 из левой
     * - Берем 9 из правой
     * Итого: 2 инверсии
     *
     * @param a - исходный массив
     * @param temp - временный массив для хранения результата слияния
     * @param left - начало левого подмассива
     * @param mid - конец левого подмассива и разделитель
     * @param right - конец правого подмассива
     * @return количество инверсий между левым и правым подмассивами
     */
    int mergeAndCount(int[] a, int[] temp, int left, int mid, int right) {
        // Инициализация указателей
        int i = left;      // Указатель на текущий элемент левой части [left...mid]
        int j = mid + 1;   // Указатель на текущий элемент правой части [mid+1...right]
        int k = left;      // Указатель на текущую позицию во временном массиве temp
        int invCount = 0;  // Счетчик инверсий между левой и правой частями

        // Основной цикл слияния: сравниваем элементы из левой и правой частей
        while (i <= mid && j <= right) {
            if (a[i] <= a[j]) {
                // Если элемент из левой части меньше или равен элементу из правой,
                // то инверсий не образуется. Просто копируем его в temp
                temp[k++] = a[i++];
            } else {
                // Если элемент из правой части меньше элемента из левой,
                // то он образует инверсии со всеми оставшимися элементами в левой части
                // Так как левая часть отсортирована, все оставшиеся элементы (a[i...mid])
                // будут больше текущего элемента a[j]
                temp[k++] = a[j++];

                // Количество инверсий = количество оставшихся элементов в левой части
                // mid - i + 1 - это количество элементов от i до mid включительно
                invCount += (mid - i + 1);
            }
        }

        // Копируем оставшиеся элементы из левой части (если есть)
        // Эти элементы уже не могут образовать инверсий, так как правая часть уже закончилась
        while (i <= mid) {
            temp[k++] = a[i++];
        }

        // Копируем оставшиеся элементы из правой части (если есть)
        // Эти элементы также не образуют новых инверсий
        while (j <= right) {
            temp[k++] = a[j++];
        }

        // Копируем отсортированные элементы из временного массива обратно в исходный
        for (i = left; i <= right; i++) {
            a[i] = temp[i];
        }

        return invCount;
    }
}
