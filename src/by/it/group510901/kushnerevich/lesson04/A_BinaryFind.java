package by.it.group510901.kushnerevich.lesson04;
import java.io.FileNotFoundException;
import java.io.InputStream;
import java.util.Scanner;

/*
В первой строке источника данных даны:
        - целое число 1<=n<=100000 (размер массива)
        - сам массив A[1...n] из n различных натуральных чисел,
          не превышающих 10E9, в порядке возрастания,
Во второй строке
        - целое число 1<=k<=10000 (сколько чисел нужно найти)
        - k натуральных чисел b1,...,bk не превышающих 10E9 (сами числа)
Для каждого i от 1 до kk необходимо вывести индекс 1<=j<=n,
для которого A[j]=bi, или -1, если такого j нет.

        Sample Input:
        5 1 5 8 12 13
        5 8 1 23 1 11

        Sample Output:
        3 1 -1 1 -1

(!) Обратите внимание на смещение начала индекса массивов JAVA относительно условий задачи
*/

public class A_BinaryFind {
    public static void main(String[] args) throws FileNotFoundException {
        // Читаем входные данные из файла dataA.txt
        InputStream stream = A_BinaryFind.class.getResourceAsStream("dataA.txt");
        A_BinaryFind instance = new A_BinaryFind();

        // Выполняем поиск индексов для каждого числа
        int[] result = instance.findIndex(stream);

        // Выводим результаты в одну строку через пробел
        for (int index : result) {
            System.out.print(index + " ");
        }
    }

    int[] findIndex(InputStream stream) throws FileNotFoundException {
        // Подготовка к чтению данных из входного потока
        Scanner scanner = new Scanner(stream);

        //!!!!!!!!!!!!!!!!!!!!!!!!!     НАЧАЛО ЗАДАЧИ     !!!!!!!!!!!!!!!!!!!!!!!!!

        // Считываем размер отсортированного массива (n)
        int n = scanner.nextInt();

        // Создаем массив a для хранения отсортированных чисел
        int[] a = new int[n];

        // Заполняем массив a числами из входного потока
        for (int i = 0; i < n; i++) {
            a[i] = scanner.nextInt();
        }

        // Поиск индексов для каждого запрашиваемого числа

        // Считываем количество чисел, которые нужно найти (k)
        int k = scanner.nextInt();

        // Создаем массив для хранения результатов поиска
        // result[i] будет содержать индекс найденного элемента (или -1)
        int[] result = new int[k];

        // Для каждого числа из второй строки выполняем бинарный поиск
        for (int i = 0; i < k; i++) {
            int value = scanner.nextInt(); // Число, которое нужно найти

            // Инициализация границ поиска
            int left = 0;      // Левая граница (индекс в массиве a)
            int right = n - 1; // Правая граница (индекс в массиве a)
            int foundIndex = -1; // Переменная для хранения найденного индекса

            // Бинарный поиск
            // Продолжаем, пока левая граница не станет больше правой
            while (left <= right) {
                // Вычисляем средний индекс
                // Используем left + (right - left) / 2 вместо (left + right) / 2
                // Это предотвращает переполнение при больших значениях left и right
                int mid = left + (right - left) / 2;

                // Проверяем, нашли ли мы искомое число
                if (a[mid] == value) {
                    // Нашли число!
                    // В условии задачи индексация начинается с 1 (а не с 0),
                    // поэтому добавляем 1 к индексу массива
                    foundIndex = mid + 1;
                    break; // Выходим из цикла, так как число найдено
                }
                // Если значение в середине меньше искомого,
                // значит искомое число находится в правой половине
                else if (a[mid] < value) {
                    left = mid + 1; // Сдвигаем левую границу вправо
                }
                // Если значение в середине больше искомого,
                // значит искомое число находится в левой половине
                else {
                    right = mid - 1; // Сдвигаем правую границу влево
                }
            }

            // Сохраняем результат поиска для текущего числа
            result[i] = foundIndex;
        }

        return result; // Возвращаем массив с найденными индексами
    }
    //!!!!!!!!!!!!!!!!!!!!!!!!!     КОНЕЦ ЗАДАЧИ     !!!!!!!!!!!!!!!!!!!!!!!!!
}
