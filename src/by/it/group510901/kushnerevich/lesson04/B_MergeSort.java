package by.it.group510901.kushnerevich.lesson04;

import java.io.FileNotFoundException;
import java.io.InputStream;
import java.util.Scanner;

/*
Реализуйте сортировку слиянием для одномерного массива.
Сложность алгоритма должна быть не хуже, чем O(n log n)

Первая строка содержит число 1<=n<=10000,
вторая - массив A[1…n], содержащий натуральные числа, не превосходящие 10E9.
Необходимо отсортировать полученный массив.

Sample Input:
5
2 3 9 2 9
Sample Output:
2 2 3 9 9
*/
public class B_MergeSort {

    public static void main(String[] args) throws FileNotFoundException {
        // Читаем входные данные из файла dataB.txt
        InputStream stream = B_MergeSort.class.getResourceAsStream("dataB.txt");
        B_MergeSort instance = new B_MergeSort();

        // Выполняем сортировку слиянием
        int[] result = instance.getMergeSort(stream);

        // Выводим отсортированный массив
        for (int index : result) {
            System.out.print(index + " ");
        }
    }

    int[] getMergeSort(InputStream stream) throws FileNotFoundException {
        // Подготовка к чтению данных
        Scanner scanner = new Scanner(stream);

        //!!!!!!!!!!!!!!!!!!!!!!!!!     НАЧАЛО ЗАДАЧИ     !!!!!!!!!!!!!!!!!!!!!!!!!

        // Чтение входных данных

        // Считываем размер массива
        int n = scanner.nextInt();

        // Создаем и заполняем массив
        int[] a = new int[n];
        for (int i = 0; i < n; i++) {
            a[i] = scanner.nextInt();
            System.out.println(a[i]); // Вывод исходных элементов (для отладки)
        }

        // Запуск сортировки слиянием
        // Вызываем рекурсивную функцию сортировки для всего массива
        mergeSort(a, 0, n - 1);

        //!!!!!!!!!!!!!!!!!!!!!!!!!     КОНЕЦ ЗАДАЧИ     !!!!!!!!!!!!!!!!!!!!!!!!!
        return a; // Возвращаем отсортированный массив
    }

    /**
     * Рекурсивная функция сортировки слиянием
     * Принцип "разделяй и властвуй" (divide and conquer):
     * 1. Делим массив на две равные (или почти равные) части
     * 2. Рекурсивно сортируем каждую часть
     * 3. Сливаем отсортированные части в один отсортированный массив
     *
     * @param a - исходный массив
     * @param left - левая граница сортируемого подмассива (включительно)
     * @param right - правая граница сортируемого подмассива (включительно)
     */
    void mergeSort(int[] a, int left, int right) {
        // Базовый случай: если подмассив состоит из одного элемента,
        // он уже отсортирован, и рекурсия останавливается
        if (left < right) {
            // Находим середину подмассива
            // Используем left + (right - left) / 2 вместо (left + right) / 2
            // для предотвращения переполнения при больших значениях
            int mid = left + (right - left) / 2;

            // Рекурсивно сортируем левую половину [left, mid]
            mergeSort(a, left, mid);

            // Рекурсивно сортируем правую половину [mid+1, right]
            mergeSort(a, mid + 1, right);

            // Сливаем две отсортированные половины в одну
            merge(a, left, mid, right);
        }
    }

    /**
     * Функция слияния двух отсортированных подмассивов
     * Подмассивы: [left...mid] и [mid+1...right]
     *
     * Алгоритм слияния:
     * 1. Создаем временные копии левой и правой частей
     * 2. Сравниваем элементы из обеих частей и вставляем меньший в результирующий массив
     * 3. Когда одна из частей заканчивается, копируем оставшиеся элементы из другой части
     *
     * @param a - исходный массив
     * @param left - начало левого подмассива
     * @param mid - конец левого подмассива и разделитель
     * @param right - конец правого подмассива
     */
    void merge(int[] a, int left, int mid, int right) {
        // Вычисляем размеры левого и правого подмассивов
        int n1 = mid - left + 1;    // Размер левого подмассива
        int n2 = right - mid;        // Размер правого подмассива

        // Создаем временные массивы для хранения копий
        int[] L = new int[n1];  // Левый временный массив
        int[] R = new int[n2];  // Правый временный массив

        // Копируем данные из исходного массива во временные массивы
        // Копируем левую половину: a[left ... mid] → L[0 ... n1-1]
        for (int i = 0; i < n1; i++)
            L[i] = a[left + i];

        // Копируем правую половину: a[mid+1 ... right] → R[0 ... n2-1]
        for (int j = 0; j < n2; j++)
            R[j] = a[mid + 1 + j];

        // Слияние двух отсортированных массивов обратно в a[left ... right]
        int i = 0;  // Индекс для прохода по левому массиву L
        int j = 0;  // Индекс для прохода по правому массиву R
        int k = left; // Индекс для записи в исходный массив a

        // Сравниваем элементы из обоих массивов и выбираем меньший
        while (i < n1 && j < n2) {
            if (L[i] <= R[j]) {     // Элемент из левого массива меньше или равен
                a[k++] = L[i++];    // Берем его из левого массива
            } else {                // Элемент из правого массива меньше
                a[k++] = R[j++];    // Берем его из правого массива
            }
        }

        // Копируем оставшиеся элементы из левого массива (если они есть)
        // Это выполняется, когда правый массив уже полностью скопирован
        while (i < n1) {
            a[k++] = L[i++];
        }

        // Копируем оставшиеся элементы из правого массива (если они есть)
        // Это выполняется, когда левый массив уже полностью скопирован
        while (j < n2) {
            a[k++] = R[j++];
        }

        // После завершения слияния, временные массивы L и R можно удалить
        // (Java сделает это автоматически через сборщик мусора)
    }
}