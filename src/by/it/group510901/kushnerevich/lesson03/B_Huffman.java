package by.it.group510901.kushnerevich.lesson03;
import java.io.FileNotFoundException;
import java.io.InputStream;
import java.util.Scanner;
import java.util.Map;
import java.util.HashMap;

// Lesson 3. B_Huffman.
// Восстановите строку по её коду и беспрефиксному коду символов.

// В первой строке входного файла заданы два целых числа
// kk и ll через пробел — количество различных букв, встречающихся в строке,
// и размер получившейся закодированной строки, соответственно.
//
// В следующих kk строках записаны коды букв в формате "letter: code".
// Ни один код не является префиксом другого.
// Буквы могут быть перечислены в любом порядке.
// В качестве букв могут встречаться лишь строчные буквы латинского алфавита;
// каждая из этих букв встречается в строке хотя бы один раз.
// Наконец, в последней строке записана закодированная строка.
// Исходная строка и коды всех букв непусты.
// Заданный код таков, что закодированная строка имеет минимальный возможный размер.
//
//        Sample Input 1:
//        1 1
//        a: 0
//        0

//        Sample Output 1:
//        a


//        Sample Input 2:
//        4 14
//        a: 0
//        b: 10
//        c: 110
//        d: 111
//        01001100100111

//        Sample Output 2:
//        abacabad

public class B_Huffman {

    public static void main(String[] args) throws FileNotFoundException {
        // Читаем входные данные из файла dataB.txt
        InputStream inputStream = B_Huffman.class.getResourceAsStream("dataB.txt");
        B_Huffman instance = new B_Huffman();

        // Декодируем строку и выводим результат
        String result = instance.decode(inputStream);
        System.out.println(result);
    }

    String decode(InputStream inputStream) throws FileNotFoundException {
        StringBuilder result = new StringBuilder(); // Для накопления декодированной строки

        // Читаем данные из входного потока
        Scanner scanner = new Scanner(inputStream);

        // Считываем количество различных букв (k) и длину закодированной строки (l)
        Integer count = scanner.nextInt();   // k - количество различных букв
        Integer length = scanner.nextInt();  // l - длина закодированной строки

        //!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! НАЧАЛО ЗАДАЧИ !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!1

        // Создаем обратное отображение (код -> символ)
        // Это позволяет быстро находить символ по его двоичному коду
        // Входные данные: для каждой буквы даны символ и его код
        // Например: a: 0, b: 10, c: 110, d: 111
        Map<String, Character> codeToChar = new HashMap<>();

        // Считываем count пар "буква: код"
        for (int i = 0; i < count; i++) {
            String letter = scanner.next(); // Считываем "a:" (с двоеточием)
            String code = scanner.next();   // Считываем код "0"

            // Из строки "a:" извлекаем первый символ - это и есть буква
            char ch = letter.charAt(0);

            // Сохраняем соответствие: код -> символ
            // Например: "0" -> 'a', "10" -> 'b', "110" -> 'c', "111" -> 'd'
            codeToChar.put(code, ch);
        }

        // Считываем закодированную строку
        String encoded = scanner.next(); // Например: "01001100100111"

        // Декодирование строки
        // Принцип: последовательно читаем биты закодированной строки,
        // накапливая их в буфере, пока не найдем совпадение с одним из кодов.
        // Так как коды префиксные (ни один код не является префиксом другого),
        // мы всегда можем однозначно определить, где заканчивается код символа.

        StringBuilder temp = new StringBuilder(); // Буфер для накопления текущего кода

        // Проходим по всем битам закодированной строки
        for (int i = 0; i < encoded.length(); i++) {
            // Добавляем очередной бит (символ '0' или '1') в буфер
            temp.append(encoded.charAt(i));

            // Проверяем, есть ли в нашем словаре код, совпадающий с содержимым буфера
            if (codeToChar.containsKey(temp.toString())) {
                // Если нашли совпадение:
                // 1. Добавляем соответствующий символ в результат
                result.append(codeToChar.get(temp.toString()));

                // 2. Очищаем буфер, чтобы начать накапливать следующий код
                temp.setLength(0);
            }
            // Если код не найден, продолжаем добавлять следующие биты
            // Это безопасно благодаря свойству префиксности кодов
        }

        //!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!! КОНЕЦ ЗАДАЧИ !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!1

        // Возвращаем декодированную строку
        return result.toString(); // Для примера: "abacabad"
    }
}